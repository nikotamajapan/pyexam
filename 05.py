
# 練習05 10進数2進数変換 (ex05.py)
# 　255以下の10進数を入力して、それを８桁の２進数で表示するプログラムを書きなさい。
# 実行結果
# 10進数> 45
# 45 = 00101101
# ...

jyu = int(input('jyu - '))
a = jyu


b7 = a // 2**7
a = a % 2**7
b6 = a // 2**6
a = a % 2**6
b5 = a // 2**5
a = a % 2**5
b4 = a // 2**4
a = a % 2**4
b3 = a // 2**3
a = a % 2**3
b2 = a // 2**2
a = a % 2**2
b1 = a // 2**1
a = a % 2**1

ni = str(b7) + str(b6) + str(b5) + str(b4) + str(b3) + str(b2) + str(b1) + str(a) 
print(jyu, 'binary=>', ni)
